#2d lists = a list of lists

drinks =["dr pepper","monster energy","root beer"]
dinner =["chicken and alfredo pizza", "triple bacon burger", "pop tart"]
desert =["chocolate overload cake", "ice cream", "cheese cake"]

food =[drinks, dinner, desert]


# so the first bracket goes to the first list ex: drinks, dinner, desert then the second bracket goes to the list in the list ex: dr pepper, monster energy, root beer
print(food[0][0])
print(food[0][1])
print(food[0][2])
print(food[1][0])
print(food[1][1])
print(food[1][2])
print(food[2][0])
print(food[2][1])
print(food[2][2])



