name = "SLYzoo"

#len = length
print(len(name))

#find, finds the letter or word in a string and says it back (it always starts from 0 not 1)
print(name.find("s"))

#capitalize only capitalizes the first letter
print(name.capitalize())

#capitalizes all letters in string
print(name.upper())

#lowercases all of the letters in the string
print(name.lower())

#is the string a digit yes or no
print(name.isdigit())

#is the string alphabeteical characters 
print(name.isalpha())

#counts how many of a selected character
print(name.count("z"))

#replaces said character with another character
print(name.replace("o", "a"))

#multiplys the var a number of times
print(name*3)
