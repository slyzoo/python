#this is a calculator that calculates area of a cube

#the user inputs the length
length = float(input("what is the length? : "))

#the user inputs the width
width = float(input("what is the width? : "))

#the user inputs the height
height = float(input("what is the height? : "))

#the code multiplies the length and width and height
volume = length * width * height

# the volume is printed in the terminal
print(f"the volume is : {volume}")
